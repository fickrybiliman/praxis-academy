public class Test1 {
   
   public static void main(String[] args) {
      int myNumber = 7;
      double d = 4.5;
      float f = 3.7f; // (float) 3.7
      char c = 'g'; // only one character
      String s1 = "Who who who!";
      System.out.println("Hello World!");
      System.out.println("Data type primitive, Integer : "  + myNumber);
      System.out.println("Data type primitive, Double ; " + d);
      System.out.println("Data type primitive, Float : " + f);
      System.out.println("Character c : " + c);
      System.out.println("String is not a primitive, String : " + s1);
      System.out.println("Concat String and Number : " + myNumber);
   }
}